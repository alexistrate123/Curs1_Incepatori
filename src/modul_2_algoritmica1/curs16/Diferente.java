package modul_2_algoritmica1.curs16;

public class Diferente {
    public static void main(String[] args) {

        // cu For
        // Afiseaza numele tau de 10 ori

        for (int i = 1; i <=10 ; i++) {
            System.out.println(i + " Alex");
        }

        System.out.println("----------------------------------------");
        // cu While

        int i = 1;
        while (i < 11) {
            System.out.println(i + " Alex");
            i++;
        }


    }
}
