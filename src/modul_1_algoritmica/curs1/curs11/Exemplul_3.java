package modul_1_algoritmica.curs1.curs11;

public class Exemplul_3 {
    public static void main(String[] args) {

        /*
        Afiseaza numerle de la 0 - 10 ;
         */
        for (int i = 0 ; i <= 10 ; i++) {
            System.out.println(i);
        }
        System.out.println("____________________________________________________________________________________");
        System.out.println();
         // afiseaza toate numerele de la 10 - 0 ;

        for (int i = 10; i >= 0 ; i--) {
            System.out.println(i);
        }
        /*
        afiseaza numerele de la 34 la 49
         */
        System.out.println("____________________________________________________________________________________");
        System.out.println();
        for (int i = 34; i <= 49 ; i++) {
            System.out.println(i);
        }




    }
}
