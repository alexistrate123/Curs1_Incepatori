package modul_1_algoritmica.curs1.curs11;

public class Incrementare {
    public static void main(String[] args) {
                  /*
                    incrementeare ( operatii )
                   */
        int i = 5;
        System.out.println(i);
        System.out.println(i+5);
        System.out.println(i);
        i += 5;
        System.out.println(i);
        i *= 10;
        System.out.println(i+20);
        i /= 2;
        System.out.println(i);
        System.out.println();
        System.out.println();
    }
}
